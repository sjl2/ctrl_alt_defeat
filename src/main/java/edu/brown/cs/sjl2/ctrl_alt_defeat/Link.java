package edu.brown.cs.sjl2.ctrl_alt_defeat;

public class Link {
  private String link;
  private String text;

  public Link(String link, String text) {
    this.link = link;
    this.text = text;
  }

  public String getLink() {
    return link;
  }

  public String getText() {
    return text;
  }
}
